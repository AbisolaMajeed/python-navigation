# Variables
FavouriteNumber = 2000000
print(f"My favourite number is : {FavouriteNumber}")

# List example
fruits = ["apple", "banana", "cherry"]
print(f"My favourite fruit is {fruits[1]}")

# Dictionary example
contacts = {
    "Alice": "555-1234",
    "Bob": "555-5678"
}
print(f"Alice's phone number is {contacts['Alice']}")